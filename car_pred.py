import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt
import numpy as np

data=pd.read_csv("data.csv")

data

data.isnull().sum()

data=data.drop(["Engine Fuel Type","Number of Doors","Transmission Type","Market Category"],axis=1)

data.head()

data.duplicated().sum()

data.drop_duplicates(inplace=True)

data

data.isnull().sum()

data.dropna(subset=["Engine HP","Engine Cylinders",],inplace=True)

data.isnull().sum()

data=pd.get_dummies(data,columns=["Driven_Wheels","Vehicle Size","Vehicle Style"],dtype=int)

data.head()

cols=["Make","Model"]
for col in cols:
  freq=data[col].value_counts(normalize=True)
  data[col]=data[col].map(freq)

data.head()

y=data["MSRP"]
x=data.drop("MSRP",axis=1)

from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test=train_test_split(x,y,test_size=.2,random_state=42)

x_train

x_train.boxplot(figsize=(15,6))
plt.show()

from sklearn.ensemble import RandomForestRegressor
model=RandomForestRegressor()
model.fit(x_train,y_train)

y_pred=model.predict(x_test)

y_pred

from sklearn.metrics import r2_score,mean_absolute_error
r2=r2_score(y_test,y_pred)
mae=mean_absolute_error(y_test,y_pred)

r2

mae

mean=data["MSRP"].mean()
error=mae / mean
error
